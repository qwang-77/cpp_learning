#include<iostream>
using namespace std;

int main() {

	// Escape character
	// Next line: \n
	// \ : \\
	// Horizontal tab: \t. Output tables more beautiful.
	cout << "hello world \n\a";

	cout << "\\" << endl;

	cout << "aaa\t" << "hello world" << endl;
	cout << "aaaaa\t" << "hello world" << endl;
	cout << "aaaaa\t" << "hello world" << endl;
	cout << "aaaa\t" << "hello world" << endl;
	cout << "aaaaaaa\t" << "hello world" << endl;


	system("pause");
	return 0;
}

